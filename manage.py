#!/usr/bin/env python
"""Точка входа для запуска административных команд Django-проекта."""

import os
import sys


def main() -> None:
    """Запускает административную команду Django.

    Функция используется как стандартная CLI-точка входа проекта.
    Она выставляет модуль настроек локального окружения по умолчанию,
    импортирует исполняющий механизм Django и передаёт ему аргументы
    командной строки текущего процесса.

    Параметры:
        Функция не принимает явных параметров. Аргументы берутся из
        ``sys.argv``.

    Возвращает:
        ``None``. После успешного завершения управление возвращается в
        операционную систему.

    Исключения:
        ImportError: Возникает, если Django не установлен в активном
            виртуальном окружении или пакет недоступен в ``PYTHONPATH``.

    Побочные эффекты:
        - Устанавливает переменную окружения ``DJANGO_SETTINGS_MODULE``,
          если она не была задана ранее.
        - Запускает команду Django, которая может читать и изменять файлы
          проекта в зависимости от переданных аргументов.
    """
    os.environ.setdefault("DJANGO_SETTINGS_MODULE", "config.settings.local")

    try:
        from django.core.management import execute_from_command_line
    except ImportError as exc:
        raise ImportError(
            "Django не установлен. Активируйте виртуальное окружение и "
            "установите зависимости из requirements/local.txt."
        ) from exc

    execute_from_command_line(sys.argv)


if __name__ == "__main__":
    main()
