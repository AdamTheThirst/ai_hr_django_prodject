"""Вспомогательные функции для чтения и преобразования переменных окружения."""

from __future__ import annotations

from pathlib import Path


TRUE_VALUES = {"1", "true", "yes", "on", "y"}
FALSE_VALUES = {"0", "false", "no", "off", "n"}


def load_env_file(env_path: Path) -> None:
    """Загружает переменные окружения из простого файла ``.env``.

    Функция нужна для локального запуска проекта без дополнительных
    зависимостей вроде ``python-dotenv``. Она читает файл построчно,
    пропускает пустые строки и комментарии, а затем записывает найденные
    пары ``KEY=VALUE`` в ``os.environ`` только если переменная ещё не была
    определена во внешнем окружении.

    Параметры:
        env_path: Абсолютный путь до файла окружения, который нужно
            прочитать.

    Возвращает:
        ``None``. Значения записываются в окружение процесса.

    Исключения:
        ValueError: Возникает, если в файле встречена строка без знака
            ``=`` и это не комментарий.

    Побочные эффекты:
        Изменяет ``os.environ`` текущего процесса для переменных, которых
        ещё не было в окружении.
    """
    import os

    if not env_path.exists():
        return

    for raw_line in env_path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" not in line:
            raise ValueError(
                f"Строка '{raw_line}' в файле {env_path} не содержит знак '='."
            )
        key, value = line.split("=", 1)
        os.environ.setdefault(key.strip(), value.strip())


def get_env(name: str, default: str | None = None) -> str | None:
    """Возвращает строковое значение переменной окружения.

    Функция используется в настройках проекта как единая точка доступа к
    переменным окружения, чтобы избежать рассыпанного по коду обращения к
    ``os.environ``.

    Параметры:
        name: Имя переменной окружения.
        default: Значение по умолчанию, которое будет возвращено, если
            переменная не задана.

    Возвращает:
        Строковое значение переменной окружения или ``default``.

    Исключения:
        Исключения не генерируются специально. Возможные ошибки среды
        выполнения Python не обрабатываются дополнительно.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """
    import os

    return os.environ.get(name, default)


def get_bool_env(name: str, default: bool = False) -> bool:
    """Преобразует переменную окружения в булево значение.

    Функция нужна для настройки признаков вроде ``DEBUG`` или флагов
    окружения. Поддерживаются типовые строковые представления истины и
    лжи в нижнем и верхнем регистре.

    Параметры:
        name: Имя переменной окружения.
        default: Значение, которое будет возвращено, если переменная не
            задана вовсе.

    Возвращает:
        ``True`` или ``False`` в зависимости от значения переменной.

    Исключения:
        ValueError: Возникает, если переменная задана, но не может быть
            интерпретирована как булево значение.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """
    raw_value = get_env(name)
    if raw_value is None:
        return default

    normalized = raw_value.strip().lower()
    if normalized in TRUE_VALUES:
        return True
    if normalized in FALSE_VALUES:
        return False
    raise ValueError(
        f"Переменная окружения {name} имеет неподдерживаемое булево значение: {raw_value}."
    )


def get_list_env(name: str, default: list[str] | None = None) -> list[str]:
    """Преобразует переменную окружения в список строк.

    Функция применяется для параметров, которые удобно хранить в одной
    строке через запятую, например ``ALLOWED_HOSTS``. Пустые элементы после
    разбиения удаляются.

    Параметры:
        name: Имя переменной окружения.
        default: Значение, которое возвращается при отсутствии переменной.

    Возвращает:
        Список непустых строк без окружающих пробелов.

    Исключения:
        Исключения не генерируются специально.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """
    raw_value = get_env(name)
    if raw_value is None:
        return default or []
    return [item.strip() for item in raw_value.split(",") if item.strip()]
