"""Временные представления приложения ``adminpanel``."""

from django.views.generic import TemplateView


class BackofficePlaceholderView(TemplateView):
    """Отображает заглушку будущей внутренней административной панели.

    Представление нужно для подготовки базовой маршрутизации ``/backoffice/``
    и демонстрации того, что под отдельный административный интерфейс уже
    выделен собственный модуль приложения.

    Параметры:
        Явные параметры не принимаются.

    Возвращает:
        ``TemplateResponse`` со страницей-заглушкой backoffice.

    Исключения:
        Возможны стандартные исключения Django при рендеринге шаблона.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """

    template_name = "adminpanel/placeholder.html"
