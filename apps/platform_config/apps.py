"""Конфигурация приложения ``platform_config``."""

from django.apps import AppConfig


class PlatformConfigConfig(AppConfig):
    """Описывает конфигурацию приложения глобальных настроек платформы.

    На первом этапе приложение подключается как самостоятельный модуль,
    чтобы в следующих итерациях в нём появились системные настройки,
    UI-тексты и параметры LLM.

    Параметры:
        Явные параметры не требуются.

    Возвращает:
        Экземпляр ``AppConfig`` для приложения ``platform_config``.

    Исключения:
        Специальные исключения не генерируются.

    Побочные эффекты:
        Регистрирует приложение в конфигурации Django.
    """

    default_auto_field = "django.db.models.BigAutoField"
    name = "apps.platform_config"
    verbose_name = "Настройки платформы"
