"""Временные представления приложения ``dialogs`` для каркаса проекта."""

from django.views.generic import TemplateView


class DialogsPlaceholderView(TemplateView):
    """Отображает заглушку раздела диалогов.

    Представление нужно для проверки, что маршрутная ветка ``/dialogs/``
    корректно подключена в корневой конфигурации URL и готова к будущему
    наполнению чатовой логикой.

    Параметры:
        Явные параметры не принимаются.

    Возвращает:
        ``TemplateResponse`` с HTML-страницей-заглушкой.

    Исключения:
        Возможны стандартные исключения рендеринга Django.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """

    template_name = "dialogs/placeholder.html"
