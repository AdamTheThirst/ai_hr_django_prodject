"""Конфигурация приложения ``dialogs``."""

from django.apps import AppConfig


class DialogsConfig(AppConfig):
    """Описывает конфигурацию приложения игровых диалогов.

    На текущем этапе приложение подключается к проекту без бизнес-логики,
    чтобы подготовить архитектурное место для будущего чата, маршрутов и
    сервисов жизненного цикла диалога.

    Параметры:
        Явные параметры не передаются. Экземпляр создаётся Django.

    Возвращает:
        Экземпляр ``AppConfig`` для приложения ``dialogs``.

    Исключения:
        Специальные исключения не генерируются.

    Побочные эффекты:
        Регистрирует приложение в конфигурации проекта.
    """

    default_auto_field = "django.db.models.BigAutoField"
    name = "apps.dialogs"
    verbose_name = "Диалоги"
