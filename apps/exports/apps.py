"""Конфигурация приложения ``exports``."""

from django.apps import AppConfig


class ExportsConfig(AppConfig):
    """Описывает конфигурацию модуля экспорта документов.

    В первой итерации приложение только регистрируется в проекте и
    подготавливает место для будущей генерации PDF без внедрения самой
    экспортной логики.

    Параметры:
        Явные параметры не требуются.

    Возвращает:
        Экземпляр ``AppConfig`` для приложения ``exports``.

    Исключения:
        Специальные исключения не генерируются.

    Побочные эффекты:
        Регистрирует приложение в реестре Django.
    """

    default_auto_field = "django.db.models.BigAutoField"
    name = "apps.exports"
    verbose_name = "Экспорт"
