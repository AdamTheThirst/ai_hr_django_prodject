"""Утилиты приложения ``accounts`` для вычисляемых пользовательских полей."""

from __future__ import annotations

import hashlib


AVATAR_BACKGROUND_PALETTE = (
    "#FDE68A",
    "#FBCFE8",
    "#BFDBFE",
    "#C7D2FE",
    "#A7F3D0",
    "#DDD6FE",
    "#FECACA",
    "#FDE68A",
)
DEFAULT_AVATAR_LETTER = "?"
DEFAULT_AVATAR_BACKGROUND = "#E5E7EB"


def build_avatar_letter(nickname: str) -> str:
    """Вычисляет отображаемую букву аватара по никнейму пользователя.

    Функция используется моделью пользователя и потенциально шаблонами,
    когда нужно показать первую букву никнейма на аватаре без загрузки
    отдельного изображения.

    Параметры:
        nickname: Отображаемое имя пользователя, из которого нужно извлечь
            первую значимую букву.

    Возвращает:
        Одну заглавную букву. Если никнейм пустой или не содержит видимых
        символов, возвращается запасной символ ``?``.

    Исключения:
        Специальные исключения не генерируются. Некорректные значения
        обрабатываются через безопасное значение по умолчанию.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """
    normalized = (nickname or "").strip()
    if not normalized:
        return DEFAULT_AVATAR_LETTER
    return normalized[0].upper()


def build_avatar_background(color_source: str) -> str:
    """Вычисляет детерминированный светлый цвет фона аватара.

    Функция применяется для согласованного отображения текстовых аватаров,
    когда пользователю нужен стабильный фон без хранения отдельного
    медиа-ресурса. Цвет выбирается из заранее ограниченной светлой палитры.

    Параметры:
        color_source: Строка-источник, по которой определяется индекс цвета,
            например email пользователя.

    Возвращает:
        HEX-значение цвета из разрешённой палитры. При пустом источнике
        возвращается безопасный цвет по умолчанию.

    Исключения:
        Специальные исключения не генерируются.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """
    if not color_source:
        return DEFAULT_AVATAR_BACKGROUND

    digest = hashlib.sha256(color_source.encode("utf-8")).hexdigest()
    palette_index = int(digest[:8], 16) % len(AVATAR_BACKGROUND_PALETTE)
    return AVATAR_BACKGROUND_PALETTE[palette_index]
