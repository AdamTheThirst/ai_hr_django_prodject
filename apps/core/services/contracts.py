"""Базовые контракты сервисного слоя проекта."""

from __future__ import annotations

from abc import ABC, abstractmethod
from typing import Generic, TypeVar


ServiceResultT = TypeVar("ServiceResultT")


class BaseService(ABC, Generic[ServiceResultT]):
    """Задаёт общий контракт для прикладных сервисов проекта.

    Абстрактный класс нужен как минимальная опора для будущего сервисного
    слоя, который по архитектуре должен отделять view от бизнес-логики.
    На текущем этапе он лишь фиксирует единый метод ``execute`` без
    привязки к конкретным предметным сценариям.

    Параметры:
        Экземпляры конкретных сервисов будут принимать собственные
        параметры через конструктор или метод ``execute`` в следующих
        итерациях. Сам базовый класс явных параметров не требует.

    Возвращает:
        Конкретное значение типа ``ServiceResultT``, определяемое дочерним
        сервисом.

    Исключения:
        NotImplementedError: Может возникнуть, если дочерний класс не
            переопределит обязательный метод ``execute``.

    Побочные эффекты:
        Сам базовый класс побочных эффектов не создаёт, но допускает их в
        наследниках в зависимости от прикладного сценария.
    """

    @abstractmethod
    def execute(self) -> ServiceResultT:
        """Выполняет прикладной сервисный сценарий.

        Метод должен переопределяться в конкретных сервисах модулей
        ``dialogs``, ``analysis``, ``content`` и других приложений, когда
        появится предметная бизнес-логика.

        Параметры:
            Явные параметры не определены базовым контрактом. Конкретные
            сервисы выбирают способ передачи входных данных самостоятельно.

        Возвращает:
            Результат выполнения сервиса в согласованном типе наследника.

        Исключения:
            NotImplementedError: Возникает в абстрактном базовом классе,
                если метод вызван без переопределения.

        Побочные эффекты:
            Определяются реализацией конкретного наследника.
        """
        raise NotImplementedError
