"""Базовые абстрактные модели проекта."""

from __future__ import annotations

import uuid

from django.conf import settings
from django.db import models
from django.utils import timezone


class TimestampedModel(models.Model):
    """Добавляет в наследников стандартные временные метки создания и обновления.

    Абстрактная модель применяется ко всем сущностям, для которых по
    спецификации требуется отслеживать момент создания и последнего
    обновления записи. Она не создаёт собственной таблицы и служит только
    общим слоем для ORM-моделей проекта.

    Параметры:
        Экземпляр создаётся через конкретные дочерние модели. Явных
        параметров у самой абстрактной модели нет.

    Возвращает:
        Экземпляр дочерней модели с полями ``created_at`` и ``updated_at``.

    Исключения:
        Специальные исключения не генерируются. Поведение зависит от
        стандартного механизма сохранения Django ORM.

    Побочные эффекты:
        При сохранении объекта автоматически обновляет поле ``updated_at``.
    """

    created_at = models.DateTimeField(
        auto_now_add=True,
        verbose_name="Создано",
    )
    updated_at = models.DateTimeField(
        auto_now=True,
        verbose_name="Обновлено",
    )

    class Meta:
        """Определяет метаданные абстрактной временной модели."""

        abstract = True


class UUIDPublicIdModel(models.Model):
    """Добавляет внешний стабильный UUID-идентификатор для публичных ссылок.

    Абстрактная модель нужна для сущностей, которые не должны раскрывать
    внутренний числовой первичный ключ наружу. Значение ``public_id``
    создаётся автоматически и остаётся неизменным на протяжении жизни
    объекта.

    Параметры:
        Экземпляр создаётся через дочерние модели. Явных параметров у
        самой абстрактной модели нет.

    Возвращает:
        Экземпляр дочерней модели с автоматически заполненным ``public_id``.

    Исключения:
        Специальные исключения не генерируются. Возможны стандартные ошибки
        Django при нарушении ограничений уникальности.

    Побочные эффекты:
        При создании записи автоматически генерирует UUID.
    """

    public_id = models.UUIDField(
        default=uuid.uuid4,
        unique=True,
        editable=False,
        verbose_name="Публичный идентификатор",
    )

    class Meta:
        """Определяет метаданные абстрактной модели с публичным UUID."""

        abstract = True


class ArchivableModel(models.Model):
    """Добавляет поля и операции мягкой архивации мастер-данных.

    Абстрактная модель применяется к сущностям, которые по спецификации
    нельзя жёстко удалять без особого сценария. Вместо удаления запись
    помечается архивной и при необходимости получает время архивации.

    Параметры:
        Экземпляр создаётся через дочерние модели. Явных параметров у
        базового класса нет.

    Возвращает:
        Экземпляр дочерней модели с полями архивного состояния.

    Исключения:
        Специальные исключения не генерируются. Возможны стандартные ошибки
        ORM при сохранении объекта.

    Побочные эффекты:
        Методы ``archive`` и ``restore`` могут сохранять запись в базу
        данных, если это не отключено параметром ``commit``.
    """

    is_archived = models.BooleanField(
        default=False,
        verbose_name="Архивирована",
    )
    archived_at = models.DateTimeField(
        null=True,
        blank=True,
        verbose_name="Архивирована в",
    )

    class Meta:
        """Определяет метаданные абстрактной архивируемой модели."""

        abstract = True

    def archive(self, commit: bool = True) -> None:
        """Переводит запись в архивное состояние.

        Метод используется в приложениях, где нужно сохранить историческую
        целостность данных и исключить жёсткое удаление мастер-данных.

        Параметры:
            commit: Если ``True``, объект сразу сохраняется в базе данных.
                Если ``False``, поля меняются только в памяти, и вызывающая
                сторона должна сохранить объект самостоятельно.

        Возвращает:
            ``None``.

        Исключения:
            Возможны стандартные исключения Django ORM при сохранении
            объекта в базу данных.

        Побочные эффекты:
            - Устанавливает ``is_archived=True``.
            - Записывает текущее время в ``archived_at``.
            - При ``commit=True`` выполняет SQL-запрос ``UPDATE``.
        """
        self.is_archived = True
        self.archived_at = timezone.now()
        if commit:
            self.save(update_fields=["is_archived", "archived_at", "updated_at"])

    def restore(self, commit: bool = True) -> None:
        """Снимает архивный статус с записи.

        Метод нужен для административных сценариев, где архивированную
        сущность требуется вернуть в активное состояние без пересоздания.

        Параметры:
            commit: Если ``True``, изменения сразу сохраняются в базе данных.
                Если ``False``, объект изменяется только в памяти.

        Возвращает:
            ``None``.

        Исключения:
            Возможны стандартные ошибки Django ORM при сохранении объекта.

        Побочные эффекты:
            - Устанавливает ``is_archived=False``.
            - Очищает ``archived_at``.
            - При ``commit=True`` выполняет SQL-запрос ``UPDATE``.
        """
        self.is_archived = False
        self.archived_at = None
        if commit:
            self.save(update_fields=["is_archived", "archived_at", "updated_at"])


class OwnedModel(models.Model):
    """Добавляет связь с пользователем-владельцем или автором записи.

    Абстрактная модель предназначена для сущностей контента и других
    управляемых объектов, где необходимо знать, кто создал запись, чтобы в
    дальнейшем строить ролевые ограничения и авторское владение.

    Параметры:
        Экземпляр создаётся через дочерние модели. Явных параметров у
        базового класса нет.

    Возвращает:
        Экземпляр дочерней модели с полем ``created_by``.

    Исключения:
        Специальные исключения не генерируются. Возможны стандартные ошибки
        ORM при нарушении ссылочной целостности.

    Побочные эффекты:
        Побочные эффекты отсутствуют. Поле используется как обычная связь
        Django ORM и не изменяется автоматически.
    """

    created_by = models.ForeignKey(
        settings.AUTH_USER_MODEL,
        null=True,
        blank=True,
        on_delete=models.SET_NULL,
        related_name="%(app_label)s_%(class)s_created_set",
        verbose_name="Создано пользователем",
    )

    class Meta:
        """Определяет метаданные абстрактной модели с владельцем."""

        abstract = True
