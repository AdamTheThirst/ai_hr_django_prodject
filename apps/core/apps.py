"""Конфигурация технического приложения ``core``."""

from django.apps import AppConfig


class CoreConfig(AppConfig):
    """Описывает базовую конфигурацию приложения ``core``.

    Класс используется Django при инициализации приложения с общими
    техническими компонентами проекта. На текущем этапе он задаёт имя
    пакета, тип первичных ключей и человекочитаемое название раздела.

    Параметры:
        Django создаёт экземпляр класса автоматически и не требует
        ручной передачи параметров из прикладного кода.

    Возвращает:
        Экземпляр ``AppConfig``, используемый внутренним механизмом Django.

    Исключения:
        Специальные исключения не генерируются. Возможные ошибки связаны
        только с общим процессом инициализации Django.

    Побочные эффекты:
        Регистрирует метаданные приложения в реестре Django apps.
    """

    default_auto_field = "django.db.models.BigAutoField"
    name = "apps.core"
    verbose_name = "Ядро проекта"
