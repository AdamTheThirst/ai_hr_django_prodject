"""Представления технического уровня для базового каркаса проекта."""

from django.views.generic import TemplateView


class HomePageView(TemplateView):
    """Отображает стартовую техническую страницу каркаса проекта.

    Представление используется на первом этапе реализации, чтобы
    подтвердить корректность работы Django, шаблонов, статики и общей
    маршрутизации. Страница не содержит бизнес-логики игры и служит только
    ориентиром для локального запуска и smoke-проверок.

    Параметры:
        Явные параметры не принимаются. Django передаёт стандартный объект
        HTTP-запроса и параметры маршрута во внутренний механизм класса.

    Возвращает:
        ``TemplateResponse`` с HTML-страницей стартового экрана.

    Исключения:
        Возможны стандартные исключения Django, например при отсутствии
        шаблона или некорректной конфигурации шаблонизатора.

    Побочные эффекты:
        Побочные эффекты отсутствуют: представление только рендерит шаблон.
    """

    template_name = "core/home.html"


class HealthCheckView(TemplateView):
    """Отображает простую страницу проверки доступности проекта.

    Представление используется для ручной и автоматической smoke-проверки
    локального запуска без обращения к ещё не реализованной бизнес-логике.

    Параметры:
        Явные параметры не принимаются. Обработка запроса выполняется
        стандартной логикой ``TemplateView``.

    Возвращает:
        ``TemplateResponse`` со страницей статуса приложения.

    Исключения:
        Возможны стандартные исключения Django, связанные с рендерингом
        шаблонов или неверной конфигурацией проекта.

    Побочные эффекты:
        Побочные эффекты отсутствуют.
    """

    template_name = "core/health.html"
