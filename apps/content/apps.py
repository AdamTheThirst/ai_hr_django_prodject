"""Конфигурация приложения ``content``."""

from django.apps import AppConfig


class ContentConfig(AppConfig):
    """Описывает конфигурацию приложения игрового контента.

    В рамках первой итерации приложение ещё не хранит игры и сценарии, но
    уже регистрируется в проекте и получает собственное пространство для
    шаблонов, маршрутов и сервисного слоя.

    Параметры:
        Явные параметры не требуются. Экземпляр создаётся Django.

    Возвращает:
        Экземпляр ``AppConfig`` для приложения ``content``.

    Исключения:
        Специальные исключения не генерируются.

    Побочные эффекты:
        Регистрирует приложение в реестре Django.
    """

    default_auto_field = "django.db.models.BigAutoField"
    name = "apps.content"
    verbose_name = "Игровой контент"
