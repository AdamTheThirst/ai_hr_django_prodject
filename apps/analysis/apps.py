"""Конфигурация приложения ``analysis``."""

from django.apps import AppConfig


class AnalysisConfig(AppConfig):
    """Описывает конфигурацию приложения аналитики.

    Приложение резервирует место под будущий синхронный анализ диалогов,
    JSON-валидацию и сохранение результатов, не реализуя эту логику на
    первом инфраструктурном этапе.

    Параметры:
        Явные параметры не требуются.

    Возвращает:
        Экземпляр ``AppConfig`` для приложения ``analysis``.

    Исключения:
        Специальные исключения не генерируются.

    Побочные эффекты:
        Регистрирует приложение в проекте Django.
    """

    default_auto_field = "django.db.models.BigAutoField"
    name = "apps.analysis"
    verbose_name = "Аналитика"
